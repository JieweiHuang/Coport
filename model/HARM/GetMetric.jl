function gdown(a,x)
t,r,θ,ϕ=x;
gd=[[(-1)+2*r*(r^2+a^2*cos(θ)^2)^(-1),2*r*(r^2+a^2*cos(θ)^2)^(-1),0,(-2)*a*r*(r^2+a^2*cos(θ)^2)^(-1)*sin(θ)^2];;
[2*r*(r^2+a^2*cos(θ)^2)^(-1),1+2*r*(r^2+a^2*cos(θ)^2)^(-1),0,(-1)*a*(1+2*r*(r^2+a^2*cos(θ)^2)^(-1))*sin(θ)^2];;
[0.0,0.0,r^2+a^2*cos(θ)^2,0.0];;
[(-2)*a*r*(r^2+a^2*cos(θ)^2)^(-1)*sin(θ)^2,(-1)*a*(1+2*r*(r^2+a^2*cos(θ)^2)^(-1))*sin(θ)^2,0.0,sin(θ)^2*(r^2+a^2*cos(θ)^2+a^2*(1+2*r*(r^2+a^2*cos(θ)^2)^(-1))*sin(θ)^2)]];
gd
end

function gup(a,x)
    t,r,θ,ϕ=x;
   gp=[[(-1)*(r^2+a^2*cos(θ)^2)^(-1)*(r*(2+r)+a^2*cos(θ)^2),2*r*(r^2+a^2*cos(θ)^2)^(-1),0.0,0.0];;
    [2*r*(r^2+a^2*cos(θ)^2)^(-1),(a^2+((-2)+r)*r)*(r^2+a^2*cos(θ)^2)^(-1),0,a*(r^2+a^2*cos(θ)^2)^(-1)];;
    [0.0,0.0,(r^2+a^2*cos(θ)^2)^(-1),0.0];;
    [0.0,a*(r^2+a^2*cos(θ)^2)^(-1),0.0,(r^2+a^2*cos(θ)^2)^(-1)*csc(θ)^2]];
    gp
end
    
